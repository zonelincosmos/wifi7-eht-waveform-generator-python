"""
LDPC parity-check and encoding matrices from IEEE 802.11-2024 Annex F.

Embeds all 12 base matrices (4 code rates x 3 codeword lengths) from
Annex F Tables F-5 through F-16.  Provides quasi-cyclic expansion from
base matrix to full parity-check matrix H, and GF(2) Gaussian
elimination to derive the encoding matrix P such that
``parity = P @ info (mod 2)``.

The encoding matrix is cached so it is computed only once per
(N, R_num, R_den) combination.
"""

import numpy as np

# ======================================================================
#  Module-level cache for encoding matrices
# ======================================================================
_P_CACHE = {}


# ======================================================================
#  IEEE 802.11-2024 Annex F base matrices
#  -1 = zero block; 0..Z-1 = circulant shift amount
# ======================================================================

def get_base_matrix(n_cw, r_num, r_den):
    """Return the Annex F base matrix for the given codeword length and rate.

    Parameters
    ----------
    n_cw : int
        Codeword length (648, 1296, or 1944).
    r_num : int
        Code rate numerator.
    r_den : int
        Code rate denominator.

    Returns
    -------
    numpy.ndarray
        2-D integer array (base parity-check matrix).
    """
    rate_scale = r_num * 12 // r_den
    # rate_scale: 1/2->6, 2/3->8, 3/4->9, 5/6->10
    ri_map = {6: 1, 8: 2, 9: 3, 10: 4}
    ci_map = {648: 1, 1296: 2, 1944: 3}

    if rate_scale not in ri_map:
        raise ValueError(f"Unsupported rate {r_num}/{r_den}")
    if n_cw not in ci_map:
        raise ValueError(f"Unsupported codeword length {n_cw}")

    ri = ri_map[rate_scale]
    ci = ci_map[n_cw]
    matrix_id = ri * 10 + ci

    return _BASE_MATRICES[matrix_id]


