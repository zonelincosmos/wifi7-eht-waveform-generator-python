# ======================================================================
#  Quasi-cyclic expansion: base matrix -> full H
# ======================================================================

def expand_qc(base_matrix, z):
    """Expand a base matrix into a full parity-check matrix via QC expansion.

    Each entry ``s`` in the base matrix becomes a ``Z x Z`` block:
    - ``s == -1``: zero block
    - ``s >= 0``: identity matrix circularly shifted right by ``s`` positions

    Parameters
    ----------
    base_matrix : numpy.ndarray
        2-D integer array from ``get_base_matrix``.
    z : int
        Expansion factor (27, 54, or 81).

    Returns
    -------
    numpy.ndarray
        Full binary parity-check matrix of shape
        ``(n_rows * z, n_cols * z)`` with dtype ``np.uint8``.
    """
    mr, nc = base_matrix.shape
    h = np.zeros((mr * z, nc * z), dtype=np.uint8)
    eye = np.eye(z, dtype=np.uint8)

    for r in range(mr):
        for c in range(nc):
            s = int(base_matrix[r, c])
            if s >= 0:
                # circshift identity right by s positions
                h[r * z:(r + 1) * z, c * z:(c + 1) * z] = np.roll(eye, s, axis=1)

    return h


# ======================================================================
#  GF(2) Gaussian elimination: H -> P encoding matrix
# ======================================================================

def derive_P(h_full):
    """Derive the LDPC encoding matrix P from the full parity-check matrix.

    Partitions H = [H1 | H2] where H1 is M x K and H2 is M x M, then
    row-reduces [H2 | H1] to [I | P] over GF(2).

    The result satisfies ``parity = P @ info (mod 2)``.

    Parameters
    ----------
    h_full : numpy.ndarray
        Full binary parity-check matrix.

    Returns
    -------
    numpy.ndarray
        Encoding matrix P of shape ``(M, K)`` with dtype ``np.uint8``.

    Raises
    ------
    RuntimeError
        If H2 is singular (should not happen for valid Annex F matrices).
    """
    m, n = h_full.shape
    k = n - m

    # Partition: H = [H1 | H2]
    h1 = h_full[:, :k].astype(np.uint8)
    h2 = h_full[:, k:].astype(np.uint8)

    # Augmented matrix [H2 | H1], to be row-reduced to [I | P]
    a = np.hstack([h2, h1])  # M x (M + K)

    for col in range(m):
        # Find pivot
        pivot = -1
        for r in range(col, m):
            if a[r, col] == 1:
                pivot = r
                break
        if pivot == -1:
            raise RuntimeError(
                f"LDPC H2 submatrix is singular at column {col}"
            )

        # Swap pivot row into position
        if pivot != col:
            a[[col, pivot]] = a[[pivot, col]]

        # XOR all other rows that have a 1 in this column
        for r in range(m):
            if r != col and a[r, col] == 1:
                a[r] = a[r] ^ a[col]

    # Extract P from the right half
    return a[:, m:]


# ======================================================================
#  High-level: load LDPC encoding matrix with caching
# ======================================================================

def load_ldpc_enc_matrix(n_cw, r_num, r_den):
    """Return the LDPC encoding matrix P for the given parameters.

    ``P`` is an ``M x K`` binary matrix such that
    ``parity = P @ info_bits (mod 2)`` and
    ``codeword = [info_bits, parity]`` satisfies ``H @ codeword = 0 (mod 2)``.

    Results are cached so the expensive GF(2) elimination runs only once
    per (n_cw, r_num, r_den) combination.

    Parameters
    ----------
    n_cw : int
        Codeword length (648, 1296, or 1944).
    r_num : int
        Code rate numerator.
    r_den : int
        Code rate denominator.

    Returns
    -------
    numpy.ndarray
        Encoding matrix P of shape ``(M, K)`` with dtype ``np.uint8``.
    """
    key = (n_cw, r_num, r_den)
    if key in _P_CACHE:
        return _P_CACHE[key]

    # Determine expansion factor Z
    z_map = {648: 27, 1296: 54, 1944: 81}
    if n_cw not in z_map:
        raise ValueError(f"Unsupported codeword length {n_cw}")
    z = z_map[n_cw]

    # Get base matrix
    hbm = get_base_matrix(n_cw, r_num, r_den)

    # Expand to full parity-check matrix
    h = expand_qc(hbm, z)

    # Derive encoding matrix via GF(2) Gaussian elimination
    p = derive_P(h)

    # Validate dimensions
    m = hbm.shape[0] * z
    k = n_cw - m
    assert p.shape == (m, k), (
        f"P dimension mismatch: got {p.shape}, expected ({m}, {k})"
    )

    _P_CACHE[key] = p
    return p
